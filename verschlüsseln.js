function spellLetters(text) {
    if (typeof text !== 'string' || text.length === 0) {
        alert("Bitte gib ein gültiges Wort ein.");
        return;
    }

    let letters = [];
    for (let i = 0; i < text.length; i++) {
        letters.push(text[i]);
    }
    return letters; // Rückgabe als Array, nicht String
}
